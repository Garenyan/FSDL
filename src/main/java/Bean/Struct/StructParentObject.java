public class StructParentObject {
    private Integer x;
    private Integer y;
    private Integer z;
    private Integer num;

    public Integer getNum() {
        return num;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

    public Integer getZ() {
        return z;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public void setZ(Integer z) {
        this.z = z;
    }
}
